package introduction.dataTypes;

public class DataTyeps {
  public static void main(String[] arg) {
    int age = 10;
    float rateOfInterest = 10.2f;
    double rate = 3.1232;
    boolean isThisGood = true;
    char myChar = '@';

    System.out.println("age: " + age);
    System.out.println("rateOfInterest: " + rateOfInterest);
    System.out.println("rate: " + rate);
    System.out.println("isThisGood: " + isThisGood);
    System.out.println("myChar: " + myChar);

  }
}
